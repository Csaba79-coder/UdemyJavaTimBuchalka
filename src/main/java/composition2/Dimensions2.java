package composition2;

public class Dimensions2 {

    private int width2;
    private int height2;
    private int depth2;

    public Dimensions2(int width2, int height2, int depth2) {
        this.width2 = width2;
        this.height2 = height2;
        this.depth2 = depth2;
    }

    public int getWidth2() {
        return width2;
    }

    public void setWidth2(int width2) {
        this.width2 = width2;
    }

    public int getHeight2() {
        return height2;
    }

    public void setHeight2(int height2) {
        this.height2 = height2;
    }

    public int getDepth2() {
        return depth2;
    }

    public void setDepth2(int depth2) {
        this.depth2 = depth2;
    }
}
